package edu.univalle.battleship.model;

/**
 * Defines the possible orientations of a ship on the board.
 * <p>
 * A ship can be placed either horizontally (left to right)
 * or vertically (top to bottom).
 */
public enum Orientation {

    /**
     * Ship is placed horizontally on the board.
     */
    HORIZONTAL,

    /**
     * Ship is placed vertically on the board.
     */
    VERTICAL
}
